public class Booking {
    public String service;
    public String date;
    public String time;
    public String price;

    public String name;
    public String address;
    public String phone;

    public Booking(String service, String date, String time, String price) {
        this.service = service;
        this.date = date;
        this.time = time;
        this.price = price;
    }
}